using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

using CWA.Venda.Data;
using CWA.Venda.Entity;
using CWA.Util;
using CWA.EngineServices;

using Newtonsoft.Json;
using Newtonsoft.Json.Linq;

namespace CWA.Venda.Business
{
    public class TipoAssinaturaEntregaBusiness
    {
        private System.Int32 _erro;
        private string _msgErro;

        public System.Int32 Erro
        {
            get { return _erro; }
        }

        public string MsgErro
        {
            get { return _msgErro; }
        }

        public string GetTipoAssinaturaEntregaService(int pintTipoEntrega, int pintTipoAssinatura)
        {

            try
            {

                TipoAssinaturaEntregaData ObjData = new TipoAssinaturaEntregaData();

                TipoAssinaturaEntregaEntity lEnt = new TipoAssinaturaEntregaEntity();

                lEnt = ObjData.GetTipoAssinaturaEntrega(pintTipoEntrega, pintTipoAssinatura);

                if (ObjData.Erro != 0)
                {
                    _erro = ObjData.Erro;
                    _msgErro = ObjData.MsgErro;

                    return null;
                }
                else
                {
                    string lstrRet = JsonConvert.SerializeObject(lEnt, Formatting.None);
                    return lstrRet;
                }


            }
            catch (Exception ex)
            {
                _erro = -99;
                _msgErro = ex.Message;

                return null;
            }

        }

        public TipoAssinaturaEntregaEntity GetTipoAssinaturaEntrega(int pintTipoEntrega, int pintTipoAssinatura)
        {

            try
            {

                string lstrParametroMetros = "";
                string lstrRet = "";
                string[] lstrVetRet = null;

                GerenciaInterface ObjInterface = new GerenciaInterface();

                lstrParametroMetros = "CWA.EngineServices.InterfaceLoja,GetTipoAssinaturaEntrega;" + pintTipoEntrega.ToString() + ";" + pintTipoAssinatura.ToString();

                ObjInterface.ExecutarInterface("", "", lstrParametroMetros, "WS");

                lstrVetRet = ObjInterface.VertorRetorno;

                if (lstrVetRet != null)
                {
                    if (lstrVetRet[0] == "0")
                    {
                        lstrRet = lstrVetRet[2];
                    }

                }

                if (lstrRet != "")
                {
                    TipoAssinaturaEntregaEntity lcolEnt = new TipoAssinaturaEntregaEntity();

                    lcolEnt = (TipoAssinaturaEntregaEntity)JsonConvert.DeserializeObject(lstrRet, lcolEnt.GetType());

                    return lcolEnt;

                }
                else
                {
                    _erro = -1;
                    _msgErro = "Erro ao retornar JSON vazio";

                    return null;
                }


            }
            catch (Exception ex)
            {
                _erro = -99;
                _msgErro = ex.Message;
                return null;
            }

        }

    }
}
